# ==================================================
# WHAT IS A VARIABLE?
# ==================================================
# A variable is a named location in memory
# used to store data.

age = 21
name = "LearnWithStriver"
is_student = True

print(age)
print(name)
print(is_student)

# ==================================================
# VARIABLE ASSIGNMENT
# ==================================================

x = 10
y = 20
result = x + y

print(result)

# ==================================================
# DYNAMIC TYPING
# ==================================================
# Python allows variables to change type dynamically.

value = 100
print(value)

value = "Now I am text"
print(value)

# ==================================================
# VARIABLE NAMING RULES
# ==================================================
# - Must start with letter or underscore
# - Cannot start with number
# - No special characters except underscore
# - Case-sensitive

valid_name = 10
_valid_name = 20
value123 = 30

# ==================================================
# BEST PRACTICES
# ==================================================
# - Use meaningful names
# - Follow snake_case

total_marks = 95
user_age = 18

