# ==================================================
# ARITHMETIC OPERATORS
# ==================================================

a = 10
b = 3

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)

# ==================================================
# COMPARISON OPERATORS
# ==================================================

print(a > b)
print(a == b)
print(a != b)

# ==================================================
# LOGICAL OPERATORS
# ==================================================

x = True
y = False

print(x and y)
print(x or y)
print(not x)

# ==================================================
# ASSIGNMENT OPERATORS
# ==================================================

value = 10
value += 5
print(value)

# ==================================================
# OPERATOR PRECEDENCE
# ==================================================

result = 10 + 2 * 3
print(result)

