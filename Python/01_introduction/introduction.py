# ==================================================
# WHAT IS PROGRAMMING?
# ==================================================
# Programming is the process of giving instructions
# to a computer so that it can perform tasks.

# Computers do not think on their own.
# They strictly follow instructions written by humans.

# These instructions must be:
# - Clear
# - Logical
# - Written in a language the computer understands

# ==================================================
# WHAT IS A PROGRAM?
# ==================================================
# A program is a collection of instructions
# written to solve a specific problem.

# Examples of problems programs solve:
# - Calculating results
# - Sending messages
# - Playing videos
# - Running websites

# ==================================================
# WHAT IS A PROGRAMMING LANGUAGE?
# ==================================================
# A programming language is a medium used
# to communicate instructions to a computer.

# Different languages exist because:
# - Different problems need different tools
# - Some languages focus on performance
# - Some focus on ease of use

# ==================================================
# WHAT IS PYTHON?
# ==================================================
# Python is a high-level programming language.
# High-level means it is close to human language
# and easy to read and write.

# Python focuses on:
# - Simplicity
# - Readability
# - Productivity

# ==================================================
# WHY LEARN PYTHON FIRST?
# ==================================================
# Python is a great first language because:
# - Syntax is simple and clean
# - Less boilerplate code
# - Allows focus on logic instead of syntax
# - Used in real-world applications

# Python is widely used in:
# - Web development
# - Data science
# - Machine learning
# - Automation
# - Software development

# ==================================================
# PYTHON IS INTERPRETED
# ==================================================
# Python code is executed line by line
# using a software called the Python Interpreter.

# This makes Python:
# - Easier to debug
# - Beginner-friendly

